class IfElseIf 
{
    // Write a if statement that says
    // if a number is positive, negative or zero
    public static void main(String[] args)
    {
        int x = 0;
        if (x > 0) {
            System.out.println("positive");
        } else if (x < 0) {
            System.out.println("negative");
        } else {
            System.out.println("zero");
        }
    }
}
