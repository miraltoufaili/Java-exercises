class Fraction
{

    private int num;

    private int denom;

    public Fraction(int a, int b)
    {
        num = a;
        denom = b;
    }
    
    // Problem: add getters to your object
    // ANSWER
    public int getNumerator()
    {
        return num;
    }

    public int getDenominator()
    {
        return denom;
    }

}
